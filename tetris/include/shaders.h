/* ═══════════════════════════════════════════════════════════
   GLOW SHADER — neon glow effect on tetromino blocks
   ═══════════════════════════════════════════════════════════ */
static const char *glow_vert_src =
    "#version 330 core\n"
    "layout(location=0) in vec2 aPos;\n"
    "uniform mat4 uProjection;\n"
    "uniform mat4 uModel;\n"
    "out vec2 vUV;\n"
    "void main() {\n"
    "    vUV = aPos;\n"
    "    gl_Position = uProjection * uModel * vec4(aPos, 0.0, 1.0);\n"
    "}\n";

static const char *glow_frag_src =
    "#version 330 core\n"
    "in vec2 vUV;\n"
    "out vec4 FragColor;\n"
    "uniform vec4 uColor;\n"
    "uniform float uIntensity;\n"
    "uniform float uTime;\n"
    "\n"
    "void main() {\n"
    "    vec2 center = vUV - vec2(0.5);\n"
    "    float dist = length(center);\n"
    "    \n"
    "    /* Inner bright core */\n"
    "    float core = smoothstep(0.5, 0.1, dist);\n"
    "    \n"
    "    /* Outer glow ring */\n"
    "    float glow = exp(-dist * 3.0) * uIntensity;\n"
    "    \n"
    "    /* Pulsing effect */\n"
    "    float pulse = 0.85 + 0.15 * sin(uTime * 2.5);\n"
    "    \n"
    "    /* Edge highlight (border glow) */\n"
    "    float edge = smoothstep(0.48, 0.42, dist) - smoothstep(0.42, 0.35, dist);\n"
    "    edge *= 0.6;\n"
    "    \n"
    "    /* Inner gradient — darker center for depth */\n"
    "    float inner = smoothstep(0.0, 0.35, dist) * 0.3;\n"
    "    \n"
    "    vec3 baseColor = uColor.rgb;\n"
    "    vec3 brightColor = mix(baseColor, vec3(1.0), 0.5);\n"
    "    \n"
    "    vec3 color = baseColor * (core * 0.7 + inner) * pulse;\n"
    "    color += brightColor * edge * pulse;\n"
    "    color += baseColor * glow * pulse * 0.5;\n"
    "    \n"
    "    /* Scanline effect */\n"
    "    float scanline = 0.95 + 0.05 * sin(gl_FragCoord.y * 1.5 + uTime * 3.0);\n"
    "    color *= scanline;\n"
    "    \n"
    "    float alpha = max(core, glow * 0.5) * uColor.a;\n"
    "    FragColor = vec4(color, alpha);\n"
    "}\n";


/* ─── BRIGHT PASS — extract pixels above brightness threshold ─── */
static const char *bright_frag_src =
    "#version 330 core\n"
    "in vec2 vUV;\n"
    "out vec4 FragColor;\n"
    "uniform sampler2D uScene;\n"
    "uniform float uThreshold;\n"
    "\n"
    "void main() {\n"
    "    vec4 c = texture(uScene, vUV);\n"
    "    float brightness = dot(c.rgb, vec3(0.2126, 0.7152, 0.0722));\n"
    "    FragColor = brightness > uThreshold ? c : vec4(vec3(0.0), 1.0);\n"
    "}\n";

/* ─── GAUSSIAN BLUR — separable 9-tap blur ─── */
static const char *blur_frag_src =
    "#version 330 core\n"
    "in vec2 vUV;\n"
    "out vec4 FragColor;\n"
    "uniform sampler2D uImage;\n"
    "uniform vec2 uDirection;\n"
    "\n"
    "void main() {\n"
    "    float w[5] = float[](0.227027, 0.1945946, 0.1216216, 0.054054, 0.016216);\n"
    "    vec3 result = texture(uImage, vUV).rgb * w[0];\n"
    "    for (int i = 1; i < 5; i++) {\n"
    "        result += texture(uImage, vUV + uDirection * float(i)).rgb * w[i];\n"
    "        result += texture(uImage, vUV - uDirection * float(i)).rgb * w[i];\n"
    "    }\n"
    "    FragColor = vec4(result, 1.0);\n"
    "}\n";

/* ─── COMPOSITE — blend original scene with bloom texture ─── */
static const char *composite_frag_src =
    "#version 330 core\n"
    "in vec2 vUV;\n"
    "out vec4 FragColor;\n"
    "uniform sampler2D uScene;\n"
    "uniform sampler2D uBloom;\n"
    "uniform float uBloomStr;\n"
    "\n"
    "void main() {\n"
    "    vec3 scene = texture(uScene, vUV).rgb;\n"
    "    vec3 bloom = texture(uBloom, vUV).rgb;\n"
    "    vec3 result = scene + bloom * uBloomStr;\n"
    "    /* Subtle tone mapping to prevent harsh clipping */\n"
    "    result = result / (result + vec3(1.0));\n"
    "    /* Gamma correct for smoother look */\n"
    "    result = pow(result, vec3(1.0 / 1.1));\n"
    "    FragColor = vec4(result, 1.0);\n"
    "}\n";

